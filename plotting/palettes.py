def getPalette(palette_name):
   """
   Function getPalette
   Gets the palette with the specified name
   
   Arguments
   palette_name: Name of the palette to get. Must exist in the palettes list above
   
   Returns the palette with the specified name, or raises KeyError if no such
   palette exists
   """
   
   if not palette_name in palettes:
      raise KeyError(("%s is not a valid palette name. To see a list of "
         "available palettes call rsg.palettes.listPalettes()") % palette_name)
   # end if
   
   return palettes[palette_name]
# end if

def listPalettes(show=True, getdescriptions=True):
   """
   Function listPalettes
   Gets the list of available palette names
   
   Arguments
   show: If set (default) prints the list of palettes to the terminal as well
         as returning it
   getdescriptions: If set (default) returns and if required prints the 
         palette descriptions as well as the names.
         
   Returns the list of available palette names
   """

   # Print palette names to terminal if that's what the user wants
   if show:
      print("Currently available palettes:")
      
      for palette_name in sorted(palettes.keys()):
         print(palette_name)
         if not getdescriptions:
            print
         else:
            if ((not (palette_name in descriptions)) 
                  or (descriptions[palette_name] == "")):
               print((": Palette %s does not have a description, "
                  "please write one and add it to the descriptions dict "
                  "in palettes.py.") % palette_name)
               continue
            # end if
            print(": %s" % descriptions[palette_name])
         # end if
      # end for
   # end if
   
   if not getdescriptions:
      return palettes.keys()
   else:
      return (palettes.keys(), descriptions)
   # end if
# end if   


